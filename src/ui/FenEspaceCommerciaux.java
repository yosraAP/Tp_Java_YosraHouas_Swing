    private void btnCalculeDepenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculeDepenseActionPerformed
        boolean statut = validerSaisiData();
        Cte_Fraix c = new Cte_Fraix();
        int id_Commercial = Integer.parseInt(txtId.getText());
        double FraixTranport = Double.parseDouble(txtFxTransport.getText());
        double FraixRepas = Double.parseDouble(txtFxRepas.getText());
        double FraixVoyage = Double.parseDouble(txtfxVoyage.getText());
        double FraixHebergement = Double.parseDouble(txtFxHebergement.getText());
        double fraisTotal;
        if (CK_Super.isSelected()) {

            if (FraixTranport > c.SUPER_FRAIXTRANSPORT_150) {
                JOptionPane.showMessageDialog(null, "frais de transport doit etre inferieur a 150", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixRepas > c.SUPER_FRAIXREPAS_60) {
                JOptionPane.showMessageDialog(null, "frais de Repas doit etre inferieur a 60", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }

            if (FraixVoyage > c.SUPER_FRAIXAVION_2000) {
                JOptionPane.showMessageDialog(null, "frais de Voyage doit etre inferieur a 2000", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }

            if (FraixHebergement > c.SUPER_FRAIXHEBERGEMENT_3000) {
                JOptionPane.showMessageDialog(null, "frais de Hebergement doit etre inferieur a 3000", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            } else if ((FraixTranport <= c.SUPER_FRAIXTRANSPORT_150) && (FraixRepas <= c.SUPER_FRAIXREPAS_60) && (FraixVoyage <= c.SUPER_FRAIXAVION_2000) && (FraixHebergement <= c.SUPER_FRAIXHEBERGEMENT_3000)) {
                fraisTotal = utils.Utilitaire.CalculeDepense(FraixTranport, FraixRepas, FraixVoyage, FraixHebergement);
                txtFxTotals.setText(String.valueOf(fraisTotal));

            } else {

                txtFxTotals.setText(" ");

            }

        }

        if (CK_Senior.isSelected()) {

            if (FraixTranport > c.SENIOR_FRAIXTRANSPORT_80) {
                JOptionPane.showMessageDialog(null, "frais de transport doit etre inferieur a 80", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixRepas > c.SENIOR_FRAIXREPAS_40) {
                JOptionPane.showMessageDialog(null, "frais de Repas doit etre inferieur a 40", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixVoyage != 0) {
                JOptionPane.showMessageDialog(null, "Vous etes Senior vous devez saisir 0", "Frais Avion Non Autorise!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixHebergement > c.SENIOR_FRAIXHEBERGEMENT_1600) {
                JOptionPane.showMessageDialog(null, "frais de Hebergement doit etre inferieur a 1600", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            } else if ((FraixTranport <= c.SENIOR_FRAIXTRANSPORT_80) && (FraixRepas <= c.SENIOR_FRAIXREPAS_40) && (FraixHebergement <= c.SENIOR_FRAIXHEBERGEMENT_1600) && (FraixVoyage == 0)) {
                fraisTotal = utils.Utilitaire.CalculeDepense(FraixTranport, FraixRepas, FraixVoyage, FraixHebergement);
                txtFxTotals.setText(String.valueOf(fraisTotal));
            } else {

                txtFxTotals.setText(" ");
            }

        }
        if (CK_Junior.isSelected()) {

            if (FraixTranport > c.JUNIOR_FRAIXTRANSPORT_50) {
                JOptionPane.showMessageDialog(null, "frais de transport doit etre inferieur a 50", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixRepas > c.JUNIOR_FRAIXREPAS_25) {
                JOptionPane.showMessageDialog(null, "frais de Repas doit etre inferieur a 25", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixVoyage != 0) {
                JOptionPane.showMessageDialog(null, "Vous etes Junior vous devez saisir 0", "Frais Avion Non Autorise!", JOptionPane.ERROR_MESSAGE);
            }
            if (FraixHebergement > c.JUNIOR_FRAIXHEBERGEMENT_1200) {
                JOptionPane.showMessageDialog(null, "frais de Hebergement doit etre inferieur a 1200", "ERREUR!", JOptionPane.ERROR_MESSAGE);
            } else if ((FraixTranport <= c.JUNIOR_FRAIXTRANSPORT_50) && (FraixRepas <= c.JUNIOR_FRAIXREPAS_25) && (FraixHebergement <= c.JUNIOR_FRAIXHEBERGEMENT_1200) && (FraixVoyage == 0)) {
                fraisTotal = utils.Utilitaire.CalculeDepense(FraixTranport, FraixRepas, FraixVoyage, FraixHebergement);
                txtFxTotals.setText(String.valueOf(fraisTotal));
            } else {

                txtFxTotals.setText(" ");
            }

        }

    }//GEN-LAST:event_btnCalculeDepenseActionPerformed

