    private void btnAjouterCommercialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterCommercialActionPerformed
        Boolean status = validerData();

        if (status) {//valider que les champs sont saisie
            String categories = "junior";
            if (CK_Super.isSelected()) {
                categories = "super ";
            }
            if (CK_Senior.isSelected()) {
                categories = "senior ";
            }
            if (CK_Junior.isSelected()) {
                categories = "junior ";
            }
            Commercial com = new Commercial(Integer.parseInt(txtId.getText()), txtNom.getText(), txtPrenom.getText(), txtEmail.getText(), txtTel.getText(), categories);
            try {
                listing.ajouterCommercial(com);
            } catch (CommercialDoublonException e) {
                JOptionPane.showMessageDialog(null, e.getCom().toString(), "Erreur Ajout", JOptionPane.ERROR_MESSAGE);
            }
        }

        listingCommerciaux();
        
        
        
    }//GEN-LAST:event_btnAjouterCommercialActionPerformed

