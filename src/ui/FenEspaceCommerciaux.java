    private boolean validerSaisiData() {
        Boolean flag = true;

        try {
            Integer.parseInt(txtId.getText());
        } catch (NumberFormatException e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "Erreur de saisie sur ID", "Saisie des valeurs", JOptionPane.ERROR_MESSAGE);
        }
        try {
            Double.parseDouble(txtFxTransport.getText());
        } catch (NumberFormatException e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "Erreur de saisie sur Frais Transport", "Saisie des valeurs", JOptionPane.ERROR_MESSAGE);
        }
        try {
            Double.parseDouble(txtFxRepas.getText());
        } catch (NumberFormatException e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "Erreur de saisie sur Frais Repas", "Saisie des valeurs", JOptionPane.ERROR_MESSAGE);
        }
        try {
            Double.parseDouble(txtfxVoyage.getText());
        } catch (NumberFormatException e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "Erreur de saisie sur Frais Voyage", "Saisie des valeurs", JOptionPane.ERROR_MESSAGE);
        }
        try {
            Double.parseDouble(txtFxHebergement.getText());
        } catch (NumberFormatException e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "Erreur de saisie sur Frais Hebergement", "Saisie des valeurs", JOptionPane.ERROR_MESSAGE);
        }

        return flag;
    }

