     private void listingCommerciaux() {
        DefaultTableModel model = (DefaultTableModel)ListeCommerciaux.getModel();
        
        //Afficher table
        
        //Vider JTable table
        model.setRowCount(0);
        //Alimenter le JTable
        
        for (Commercial com : listing.getRegistre()){
            model.addRow(new Object[]{com.getId_commercial(), com.getNom(), com.getPrenom(), com.getEmail(),com.getTel(),com.getCategories()});
        }
    }

